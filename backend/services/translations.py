def get_translation(lang_code, key, default=''):
    """Get translation for a specific key in the given language."""
    lang_code = lang_code or 'en'
    translations = UI_TRANSLATIONS.get(lang_code, UI_TRANSLATIONS['en'])
    return translations.get(key, default or key)


def get_interpretation_template(lang_code, key, **kwargs):
    """Get interpretation template with variable substitution."""
    lang_code = lang_code or 'en'
    templates = INTERPRETATION_TEMPLATES.get(lang_code, INTERPRETATION_TEMPLATES['en'])
    template = templates.get(key, key)
    
    if kwargs:
        return template.format(**kwargs)
    return template


def get_insight_template(lang_code, key, **kwargs):
    """Get localized insight template with variable substitution."""
    lang_code = lang_code or 'en'
    templates = INSIGHT_TEMPLATES.get(lang_code, INSIGHT_TEMPLATES['en'])
    template = templates.get(key, key)
    
    if kwargs:
        try:
            return template.format(**kwargs)
        except KeyError:
            return template
    return template
